    private void jbtnLogin2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLogin2ActionPerformed
        try {
                 Connection con=getConnection();
                PreparedStatement ps=con.prepareStatement("SELECT * FROM LOGIN WHERE Password = ?");
                ps.setString(1,String.valueOf(jtxtPassword.getText()));
                ResultSet result=ps.executeQuery();
                if(result.next())
                {
                ps=con.prepareStatement("UPDATE Login SET Username=?, Answer=?, Password=?");
                ps.setString(1,jtxtUserName3.getText());
                ps.setString(2,jtxtAnswer.getText());
                ps.setString(3,String.valueOf(jtxtPassword1.getText()));
                //ps.setString(4,String.valueOf(jtxtPassword.getText()));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Registered");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Invalid Old Password","Registration Denied",JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
                JOptionPane.showMessageDialog(null,"Invalid Old Password","Registration Denied",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jbtnLogin2ActionPerformed

