    private void jbtnLogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLogin1ActionPerformed
        try {
                Connection con=getConnection();
                PreparedStatement ps=con.prepareStatement("SELECT Username,Password FROM LOGIN WHERE Username=? AND Password=?");
                ps.setString(1,jtxtUserName1.getText());
                ps.setString(2,String.valueOf(jPassword1.getText()));
                ResultSet result=ps.executeQuery();
                if(result.next())
                {
                    JOptionPane.showMessageDialog(null,"Login Successful");
                    setVisible(false);
                    systemExit();
                    this.hide();
                    MainPage1 mp=new MainPage1();
                    mp.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null,"Invalid Username or Password","Login Failed",JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
                JOptionPane.showMessageDialog(null,"Invalid Username or Password","Login Failed",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jbtnLogin1ActionPerformed

